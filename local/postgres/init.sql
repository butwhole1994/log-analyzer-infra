    ON log_event_raw USING gin (raw_payload);

CREATE INDEX IF NOT EXISTS idx_detection_rules_project_id
    ON detection_rules (project_id);

CREATE INDEX IF NOT EXISTS idx_detected_incidents_project_status
    ON detected_incidents (project_id, status);

CREATE INDEX IF NOT EXISTS idx_detected_incidents_service_status
    ON detected_incidents (service_id, status);

CREATE INDEX IF NOT EXISTS idx_detected_incidents_rule_id
    ON detected_incidents (rule_id);

CREATE INDEX IF NOT EXISTS idx_incident_events_incident_id
    ON incident_events (incident_id);

CREATE INDEX IF NOT EXISTS idx_incident_events_log_event_id
    ON incident_events (log_event_id);

CREATE INDEX IF NOT EXISTS idx_alert_channels_project_id
    ON alert_channels (project_id);

CREATE INDEX IF NOT EXISTS idx_alert_notifications_incident_id
    ON alert_notifications (incident_id);

CREATE INDEX IF NOT EXISTS idx_alert_notifications_channel_id
    ON alert_notifications (alert_channel_id);

CREATE INDEX IF NOT EXISTS idx_incident_comments_incident_id
    ON incident_comments (incident_id);

CREATE INDEX IF NOT EXISTS idx_audit_logs_user_created_at
    ON audit_logs (user_id, created_at DESC);

CREATE INDEX IF NOT EXISTS idx_audit_logs_project_created_at
    ON audit_logs (project_id, created_at DESC);

CREATE INDEX IF NOT EXISTS idx_audit_logs_target
    ON audit_logs (target_type, target_id);

