    ON log_events (log_source_id, occurred_at DESC);

CREATE INDEX IF NOT EXISTS idx_log_events_batch_id
    ON log_events (ingest_batch_id);

CREATE INDEX IF NOT EXISTS idx_log_events_trace_id
    ON log_events (trace_id);

CREATE INDEX IF NOT EXISTS idx_log_events_level_occurred_at
    ON log_events (level, occurred_at DESC);

CREATE INDEX IF NOT EXISTS idx_log_events_http_status_occurred_at
    ON log_events (http_status, occurred_at DESC);

